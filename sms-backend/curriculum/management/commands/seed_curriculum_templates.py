class Command(BaseCommand):
    help = "Seed CBE-aligned starter templates for Schemes of Work (all subjects, Grade 7-10)"

    def add_arguments(self, parser):
        parser.add_argument(
            '--schema',
            default='demo_school',
            help='Tenant schema name (default: demo_school)',
        )
        parser.add_argument(
            '--all-tenants',
            action='store_true',
            help='Seed curriculum templates for all non-public tenant schemas',
        )

    def handle(self, *args, **options):
        from django_tenants.utils import schema_context, get_tenant_model

        if options.get('all_tenants'):
            TenantModel = get_tenant_model()
            schemas = list(
                TenantModel.objects.exclude(schema_name='public')
                                   .values_list('schema_name', flat=True)
            )
        else:
            schemas = [options['schema']]

        for schema in schemas:
            self.stdout.write(f"Seeding CBE curriculum templates into schema '{schema}'...")
            with schema_context(schema):
                self._seed(schema)

    def _seed(self, schema):
        from school.models import Subject
        from curriculum.models import SchemeOfWork, SchemeTopic

        # Rename any legacy "CBC ..." or "KCSE ..." templates
        for scheme in SchemeOfWork.objects.filter(is_template=True):
            changed = False
            for field in ('template_name', 'title', 'objectives', 'template_description'):
                val = getattr(scheme, field, '') or ''
                new_val = val.replace("CBC", "CBE").replace("KCSE", "CBE National Assessment")
                if new_val != val:
                    setattr(scheme, field, new_val)
                    changed = True
            if changed:
                scheme.save(update_fields=["template_name", "title", "objectives", "template_description"])

        created_count = 0
        skipped_count = 0

        for tmpl in TEMPLATES:
            subject = Subject.objects.filter(name__iexact=tmpl['subject_name']).first()
            if not subject:
                self.stdout.write(
                    self.style.WARNING(
                        f"  Subject '{tmpl['subject_name']}' not found — skipping"
                    )
                )
                skipped_count += 1
                continue

            scheme, created = SchemeOfWork.objects.get_or_create(
                subject=subject,
                school_class=None,
                term=None,
                is_template=True,
                template_name=tmpl['template_name'],
                defaults={
                    'title': tmpl['title'],
                    'objectives': tmpl['objectives'],
                    'template_description': tmpl['template_description'],
                },
            )

            if not created:
                self.stdout.write(f"  Skipped (already exists): {tmpl['template_name']}")
                skipped_count += 1
                continue

            SchemeTopic.objects.bulk_create([
                SchemeTopic(
                    scheme=scheme,
                    week_number=t['week_number'],
                    topic=t['topic'],
                    sub_topics=t.get('sub_topics', ''),
                    learning_outcomes=t.get('learning_outcomes', ''),
                    teaching_methods=t.get('teaching_methods', ''),
                    resources=t.get('resources', ''),
                    assessment_type=t.get('assessment_type', ''),
                    is_covered=False,
                )
                for t in tmpl['topics']
            ])

            self.stdout.write(
                self.style.SUCCESS(
                    f"  Created: {tmpl['template_name']} ({len(tmpl['topics'])} topics)"
                )
            )
            created_count += 1

        self.stdout.write(
            self.style.SUCCESS(
                f"\nDone — {created_count} template(s) created, {skipped_count} skipped."
            )
        )
